#include <stdio.h>

int main() {
  printf("1234 + 5678 = %d\n", 1234 + 5678);
  printf("1234 - 5678 = %d\n", 1234 - 5678);
  printf("0x1a = %d\n", 0x1a);
  printf("5 * 5 = %d\n", 5 * 5);
  printf("10 / 3 = %d\n", 10 / 3);
  printf("10.0 / 3 = %f\n", 10.0 / 3);
  printf("10.0 / 3 ~= %.0f\n", 10.0 / 3);
  return 0;
}
